#include "get_next_line.h"

char	*get_next_line(int fd)
{
	char	*line;
	size_t	size;

	size = get_next_line_size(fd);
	line = malloc(sizeof(char) * size + 1);
	if (!line)
		return (0);
	read(fd, line, size);
	return (line);
}
